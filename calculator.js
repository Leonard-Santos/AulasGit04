function soma(num1, num2){
    return num1 + num2;
}

function subtracao(num1, num2){
    return num1 - num2;
}

function multipli(num1, num2){
    return num1 * num2;
}

function divisao(num1, num2){
    return num1 / num2;
}

exports.multipli = multipli;
exports.divisao = divisao;
exports.soma = soma;
exports. subtracao = subtracao;